// Plain-text join of a passage's verses
export function passageText(ref) {
  const p = PASSAGES[ref]
  if (!p) return ''
  return p.verses.map((v) => v.t).join(' ')
}

export function passageList() {
  return Object.keys(PASSAGES)
}
